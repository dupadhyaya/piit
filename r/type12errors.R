# Type I, II errors

#TI----
#A Type I error occurs when a true null hypothesis is incorrectly rejected. This means concluding there is an effect or difference when, in reality, there is none. The probability of making a Type I error is denoted by alpha (\(\alpha \)), which is the significance level chosen for the test (e.g., 0.05). 

#TII-----
#Type II Error (False Negative):   A Type II error occurs when a false null hypothesis is incorrectly not rejected. This means failing to detect an effect or difference that truly exists. The probability of making a Type II error is denoted by beta (\(\beta \)), and it is inversely related to the power of the test (Power = 1 - \(\beta \)). 

